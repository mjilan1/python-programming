disney_character = ["simba", "ariel", "pumba", "flouder", "nala", "urusula", "scar", "flotsam", "timon"]

for character in disney_character:
    if "u" in character:
        print(character, "U are so Uniquely U")
    elif "i" in character:
        print(character, "I bet youre Impressively Intelligent")
    elif "o" in character:
        print(character, "O MY! How Original!")
    else:
        print(character, "Ehh, a's and e's are so ordinary")